package com.emicalculator.emi.dto;

public class CalculoAposentadoriaDTO {

    private double patrimonioFinal;
    private double valorHeranca;
    private double gastoMensal;
    private double valorMeta;
    
	public double getPatrimonioFinal() {
		return patrimonioFinal;
	}
	public void setPatrimonioFinal(double patrimonioFinal) {
		this.patrimonioFinal = patrimonioFinal;
	}
	public double getValorHeranca() {
		return valorHeranca;
	}
	public void setValorHeranca(double valorHeranca) {
		this.valorHeranca = valorHeranca;
	}
	public double getGastoMensal() {
		return gastoMensal;
	}
	public void setGastoMensal(double gastoMensal) {
		this.gastoMensal = gastoMensal;
	}
	public double getValorMeta() {
		return valorMeta;
	}
	public void setValorMeta(double valorMeta) {
		this.valorMeta = valorMeta;
	}

}
